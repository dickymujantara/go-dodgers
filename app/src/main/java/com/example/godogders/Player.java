package com.example.godogders;

public class Player {
    private String name,nickname,position,height;
    private int age,noJersey,weight,photo;
    private char bat,throwing;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getNoJersey() {
        return noJersey;
    }

    public void setNoJersey(int noJersey) {
        this.noJersey = noJersey;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public char getBat() {
        return bat;
    }

    public void setBat(char bat) {
        this.bat = bat;
    }

    public char getThrowing() {
        return throwing;
    }

    public void setThrowing(char throwing) {
        this.throwing = throwing;
    }
}
